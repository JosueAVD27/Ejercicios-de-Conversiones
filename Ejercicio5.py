#Ingresar el radio de un círculo del usuario y calcule el área. area = pi*radio^2
print("<<< Calcular el Area de un circulo >>>")
radio = float(input("Ingrese el radio del circulo: "))  #Pide ingresar el radio por teclado al usuario.
pi = 3.1416                                             #Crea la variable pi
area = pi * (radio*radio)                               #Realiza los calculos en base a una formula dada.
print("El area es: ", area)                             #Entrega el resultado realizado.

